    public void chonHinh() {
        fileimage = new GameDAO().chonAnh(lblImage);
    }

    public void insertItem() {

        try {
            Game cd = new Model.Game(txtMaTL.getText(),
                    txtTenTL.getText(),
                    fileimage,
                    txtNote.getText());
            new GameDAO().insertItem(cd);
        } catch (Exception e) {
        }
    }

    int a = 0;
    public void showDetail(int a) throws IOException {
        Game cd = new GameDAO().hienthi(tblGrid,a);
        txtMaTL.setText(cd.getMaTL());
        txtTenTL.setText(cd.getTenTL());
        txtNote.setText(cd.getMota());
        fileimage = cd.getHinh();
        BufferedImage img = ImageIO.read(new File("src\\icon\\" + fileimage));
        Image dmg = img.getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon icon = new ImageIcon(dmg);
        lblImage.setIcon(icon);
    }

    public void updateItem() {
        Game cd = new Game(txtMaTL.getText(),
                txtTenTL.getText(),
                fileimage,
                txtNote.getText());
        new GameDAO().updateItem(cd);
    }

    public void deleteItem() {
        int r = JOptionPane.showConfirmDialog(null, "Do you want Delete?");
        if (r == JOptionPane.YES_OPTION) {
            Game cd = new Game(txtMaTL.getText(), txtTenTL.getText(),
                    fileimage, txtNote.getText());
            new GameDAO().deleteItem(cd);
        }
    }

