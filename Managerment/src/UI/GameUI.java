    public GameUI() {
        initComponents();
        showList();
        this.setTitle("Kind of game");
        this.setLocationRelativeTo(null);
        Thread cl = new Thread() {
            public void run() {
                while (true) {
                    try {
                        Date now = new Date();
                        SimpleDateFormat fm = new SimpleDateFormat();
                        fm.applyPattern("hh:mm:ss");
                        String time = fm.format(now);
                        lblClock.setText(time);
                        Thread.sleep(1000);
                    } catch (InterruptedException e) {
                        break;
                    }
                }
            }
        };
        cl.start();
    }

    public boolean check() {
        if (txtMaTL.getText().isEmpty() && txtTenTL.getText().isEmpty()
                && txtNote.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập dữ liệu");
        } else if (txtMaTL.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã chuyên thể loại");
        } else if (txtTenTL.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên thể loại");
        } else if (txtNote.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống ghi chú");
        } else {
            JOptionPane.showMessageDialog(this, "Thao tác thành công!");
        }
        return false;

    }


